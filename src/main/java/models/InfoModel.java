package models;

public class InfoModel {
    private String description;
    private String namespace;
    private String name;
    private String brief;

    public String getDescription() {
        return description;
    }

    public String getNameSpace() {
        return namespace;
    }

    public String getName() {
        return name;
    }

    public String getBrief() {
        return brief;
    }
}
